DROP TABLE TimeSlot ;
CREATE TABLE `TimeSlot` (
  `TimeSlotId` int NOT NULL AUTO_INCREMENT,
  `DayId` int DEFAULT NULL,
  `PeriodId` int DEFAULT NULL,
  PRIMARY KEY (`TimeSlotId`),
  KEY `FK_DayId` (`DayId`),
  KEY `FK_PeriodId` (`PeriodId`),
  CONSTRAINT `FK_DayId` FOREIGN KEY (`DayId`) REFERENCES `Day` (`DayId`),
  CONSTRAINT `FK_PeriodId` FOREIGN KEY (`PeriodId`) REFERENCES `Period` (`PeriodId`)
) 

SHOW CREATE TABLE Course;
DROP TABLE Course;
CREATE TABLE `Course` (
  `CourseId` int NOT NULL AUTO_INCREMENT,
  `CourseCatalogId` int NOT NULL,
  `TimeSlotOneId` int  DEFAULT NULL,
  PRIMARY KEY (`CourseId`),
  KEY `FK_CourseCatalogId` (`CourseCatalogId`),
  KEY `FK_CourseTimeSlotOne` (`TimeSlotOneId`),
  CONSTRAINT `FK_CourseCatalogId` FOREIGN KEY (`CourseCatalogId`) REFERENCES `CourseCatalog` (`CourseCatalogId`),
  CONSTRAINT `FK_CourseTimeSlotOne` FOREIGN KEY (`TimeSlotOneId`) REFERENCES `TimeSlot` (`TimeSlotId`)
) 

